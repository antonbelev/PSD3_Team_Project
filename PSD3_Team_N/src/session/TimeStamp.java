package session;

public class TimeStamp {
	
	private int startH;
	private int endH;
	
	public TimeStamp(int startH, int endH) {
		this.startH = startH;
		this.endH = endH;
	}
	
	public int getStartH() {
		return startH;
	}
	public void setStartH(int startH) {
		this.startH = startH;
	}
	public int getEndH() {
		return endH;
	}
	public void setEndH(int endH) {
		this.endH = endH;
	}
	
	

}
